    def _draw_world(self, s):
        lvl = self.level
        cam = int(self.cam_x)
        s.fill(lvl.bg)
        # decor
        for d in self.decor:
            if d[0] == "cloud":
                _, x, y = d
                if -40 < x - cam < NATIVE_W: draw_cloud(s, x - cam, y)
            elif d[0] == "hill":
                _, x, y, big = d
                if -60 < x - cam < NATIVE_W: draw_hill(s, x - cam, y, big)
            elif d[0] == "bush":
                _, x, y = d
                if -40 < x - cam < NATIVE_W: draw_bush(s, x - cam, y)

        # tiles
        c0 = cam // TILE
        c1 = c0 + COLS_VIS + 2
        bumpmap = {(b[0], b[1]): b[2] for b in self.bumps}
        for col in range(c0, c1):
            for row in range(ROWS):
                ch = lvl.get(col, row)
                if ch == "-" or ch == "|" or ch == "X":
                    continue
                dy = 0
                if (col, row) in bumpmap:
                    dy = int(bumpmap[(col, row)])
                if ch == "#": img = make_tile("ground", lvl.theme)
                elif ch == "B": img = make_tile("brick", lvl.theme)
                elif ch == "?": img = make_tile("qblock", lvl.theme)
                elif ch == "Q": img = make_tile("used", lvl.theme)
                elif ch == "S": img = make_tile("stair", lvl.theme)
                elif ch == "L": img = make_tile("lava", lvl.theme)
                else: continue
                s.blit(img, (col*TILE - cam, row*TILE + dy))

        # pipes
        for (pc, ptop, ph, _) in lvl.pipes:
            self._draw_pipe(s, pc, ptop, ph, cam)

        # flagpole / axe
        fc = lvl.flag_col
        if c0 <= fc <= c1:
            px = fc*TILE - cam + 6
            pygame.draw.rect(s, C["pole_dk"], (px, TILE, 4, (ROWS-3)*TILE))
            pygame.draw.rect(s, C["pole"], (px, TILE, 2, (ROWS-3)*TILE))
            pygame.draw.circle(s, C["star"], (px+2, TILE), 4)
            if not lvl.is_castle:
                pygame.draw.polygon(s, C["flag"], [(px-2, TILE+6), (px-2, TILE+18), (px-14, TILE+12)])
            else:
                pygame.draw.rect(s, C["star"], (px-8, TILE-2, 8, 6))  # axe

        # entities
        for it in self.items: it.draw(s, cam)
        for sh in self.shards: sh.draw(s, cam)
        for cp in self.popups: cp.draw(s, cam)
        for e in self.enemies:
            if e.x - cam < NATIVE_W + 20 and e.x - cam > -30:
                e.draw(s, cam)
        for f in self.fireballs: f.draw(s, cam)
        self.player.draw(s, cam)

        self._draw_hud(s)
        if self.msg_timer > 0:
            self._center(s, f"WORLD {self.world}-{self.stage}", self.bigfont, C["white"], NATIVE_H//2 - 12)
        if self.state == self.STATE_CLEAR:
            self._center(s, "COURSE CLEAR!", self.bigfont, C["star"], 40)

    def _draw_pipe(self, s, pc, ptop, ph, cam):
        x = pc*TILE - cam
        w = 2*TILE
        # body
        for r in range(ph):
            yy = (ptop + r) * TILE
            pygame.draw.rect(s, C["pipe_dk"], (x, yy, w, TILE))
            pygame.draw.rect(s, C["pipe"], (x+2, yy, w-6, TILE))
            pygame.draw.rect(s, C["pipe_lt"], (x+3, yy, 3, TILE))
        # rim
        yy = ptop * TILE
        pygame.draw.rect(s, C["pipe_dk"], (x-2, yy, w+4, 6))
        pygame.draw.rect(s, C["pipe"], (x, yy, w, 4))
        pygame.draw.rect(s, C["pipe_lt"], (x+1, yy, 4, 4))

    def _draw_hud(self, s):
        def txt(t, x, y, col=C["white"]):
            s.blit(self.font.render(t, False, col), (x, y))
        txt("MARIO", 12, 6)
        txt(f"{self.score:06d}", 12, 16)
        txt("x{:02d}".format(self.coins), 92, 16, C["star"])
        pygame.draw.circle(s, C["coin"], (88, 19), 3)
        txt("WORLD", 150, 6)
        txt(f"{self.world}-{self.stage}", 158, 16)
        txt("TIME", 210, 6)
        txt(f"{max(0,self.time):03d}", 214, 16)
        txt(f"LIVES {self.lives}", 12, 28, C["m_red"])


