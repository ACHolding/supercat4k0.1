class Entity:
    def __init__(self, x, y, w, h):
        self.x = float(x); self.y = float(y)
        self.w = w; self.h = h
        self.vx = 0.0; self.vy = 0.0
        self.alive = True
        self.on_ground = False
    @property
    def rect(self):
        return pygame.Rect(int(self.x), int(self.y), self.w, self.h)
    def cx(self): return self.x + self.w / 2

def tile_collide_x(ent, level):
    ent.x += ent.vx
    r = ent.rect
    c0 = int(r.left // TILE); c1 = int(r.right // TILE)
    rr0 = int(r.top // TILE); rr1 = int(r.bottom // TILE)
    hit = False
    for cc in range(c0, c1 + 1):
        for rrw in range(rr0, rr1 + 1):
            if level.solid(cc, rrw):
                tr = pygame.Rect(cc*TILE, rrw*TILE, TILE, TILE)
                if r.colliderect(tr):
                    if ent.vx > 0:
                        ent.x = tr.left - ent.w
                    elif ent.vx < 0:
                        ent.x = tr.right
                    ent.vx = 0; hit = True
                    r = ent.rect
    return hit

def tile_collide_y(ent, level):
    ent.y += ent.vy
    r = ent.rect
    c0 = int(r.left // TILE); c1 = int(r.right // TILE)
    rr0 = int(r.top // TILE); rr1 = int(r.bottom // TILE)
    ent.on_ground = False
    head_tile = None
    for cc in range(c0, c1 + 1):
        for rrw in range(rr0, rr1 + 1):
            if level.solid(cc, rrw):
                tr = pygame.Rect(cc*TILE, rrw*TILE, TILE, TILE)
                if r.colliderect(tr):
                    if ent.vy > 0:
                        ent.y = tr.top - ent.h
                        ent.on_ground = True
                    elif ent.vy < 0:
                        ent.y = tr.bottom
                        head_tile = (cc, rrw)
                    ent.vy = 0
                    r = ent.rect
    return head_tile

def pipe_collide(ent, level):
    r = ent.rect
    for (pc, ptop, ph, _) in level.pipes:
        pr = pygame.Rect(pc*TILE, ptop*TILE, 2*TILE, ph*TILE)
        if r.colliderect(pr):
            # resolve simply: push out on smallest axis
            dx = min(r.right - pr.left, pr.right - r.left)
            dy = min(r.bottom - pr.top, pr.bottom - r.top)
            if dx < dy:
                if ent.cx() < pr.centerx:
                    ent.x = pr.left - ent.w
                else:
                    ent.x = pr.right
                ent.vx = 0
            else:
                if ent.y < pr.top:
                    ent.y = pr.top - ent.h
                    ent.on_ground = True
                    ent.vy = 0
                else:
                    ent.y = pr.bottom
                    ent.vy = 0
            r = ent.rect


class Player(Entity):
    def __init__(self, x, y):
        super().__init__(x, y, 12, 14)
        self.power = 0          # 0 small, 1 big, 2 fire
        self.facing = 1
        self.anim = 0.0
        self.invuln = 0
        self.star = 0
        self.crouch = False
        self.dead = False
        self.win = 0            # >0 = level-complete sequence timer
        self.fire_cd = 0
        self.prev_jump = False  # SMB jump state
        self.holding_jump = False
        self.jump_tier = 0

    def set_power(self, p):
        was = self.power
        self.power = p
        if p >= 1 and was == 0:
            self.h = 28
            self.y -= 14
        elif p == 0 and was >= 1:
            self.h = 14
            self.y += 14

    def hurt(self, game):
        if self.invuln > 0 or self.star > 0 or self.win:
            return
        if self.power >= 1:
            self.set_power(0)
            self.invuln = 90
            game.audio.play("power")
        else:
            self.die(game)

    def die(self, game):
        if self.dead:
            return
        self.dead = True
        self.vy = -8
        self.vx = 0
        game.audio.stop_music()
        game.audio.play("die")

    def update(self, game, keys):
        if self.dead:
            self.vy += GRAVITY
            self.y += self.vy
            return
        if self.win:
            return
        lvl = game.level
        left = keys[pygame.K_LEFT] or keys[pygame.K_a]
        right = keys[pygame.K_RIGHT] or keys[pygame.K_d]
        run = keys[pygame.K_x] or keys[pygame.K_LSHIFT] or keys[pygame.K_RSHIFT]
        down = keys[pygame.K_DOWN] or keys[pygame.K_s]
        self.crouch = down and self.power >= 1 and self.on_ground

        # ---- SMB1 horizontal movement (subpixel-accurate) ----
        if self.crouch:
            left = right = False
        move = -1 if (left and not right) else (1 if (right and not left) else 0)

        if move != 0:
            self.facing = move
            if self.vx * move < 0:                  # pressing against motion -> skid
                self.vx += P_SKID_DEC * move
                if abs(self.vx) < P_MIN_WALK:
                    self.vx = P_MIN_WALK * move      # finished turning around
            else:
                if abs(self.vx) < P_MIN_WALK:
                    self.vx = P_MIN_WALK * move
                self.vx += (P_RUN_ACC if run else P_WALK_ACC) * move
                cap = P_MAX_RUN if run else P_MAX_WALK
                if not run and abs(self.vx) > P_MAX_WALK:
                    self.vx -= P_DECEL * move         # released run: ease to walk cap
                elif abs(self.vx) > cap:
                    self.vx = cap * move
        else:
            if self.vx > 0:   self.vx = max(0.0, self.vx - P_DECEL)
            elif self.vx < 0: self.vx = min(0.0, self.vx + P_DECEL)
        self.vx = max(-P_MAX_RUN, min(P_MAX_RUN, self.vx))

        # ---- SMB1 jump: constant launch, speed-tiered & button-held gravity ----
        jb = keys[pygame.K_z] or keys[pygame.K_SPACE] or keys[pygame.K_UP]
        if jb and not self.prev_jump and self.on_ground:
            self.vy = P_JUMP_VY
            self.on_ground = False
            self.holding_jump = True
            sp = abs(self.vx)
            self.jump_tier = 2 if sp >= P_TIER_FAST else (1 if sp >= P_TIER_MID else 0)
            game.audio.play("jump")
        if not jb:
            self.holding_jump = False
        self.prev_jump = jb
        if self.vy < 0 and self.holding_jump:
            self.vy += P_GRAV_HOLD[self.jump_tier]    # floaty rise while held
        else:
            self.vy += P_GRAV_FALL[self.jump_tier]    # snappier fall / released
        self.vy = min(self.vy, P_MAX_FALL)

        # fire
        if self.fire_cd > 0: self.fire_cd -= 1
        if run and self.power == 2 and self.fire_cd == 0 and len([f for f in game.fireballs if f.alive]) < 2:
            fx = self.x + (self.w if self.facing > 0 else -4)
            game.fireballs.append(Fireball(fx, self.y + 6, self.facing))
            self.fire_cd = 18
            game.audio.play("fire")

        # collisions
        tile_collide_x(self, lvl)
        head = tile_collide_y(self, lvl)
        pipe_collide(self, lvl)
        if head:
            self._hit_block(game, head[0], head[1])

        # bounds
        if self.x < game.cam_x:
            self.x = game.cam_x; self.vx = 0
        if self.x < 0:
            self.x = 0

        # fall in pit / lava
        if self.y > NATIVE_H + 20:
            self.die(game)
        rr = int((self.y + self.h - 1) // TILE)
        cc = int(self.cx() // TILE)
        if lvl.get(cc, rr) == "L":
            self.die(game)

        if self.invuln > 0: self.invuln -= 1
        if self.star > 0: self.star -= 1

        # animation
        if abs(self.vx) > 0.2 and self.on_ground:
            self.anim += abs(self.vx) * 0.12
        else:
            self.anim = 0

        # reach flag
        if self.x + self.w >= lvl.flag_col * TILE and not self.win:
            self.win = 1
            self.x = lvl.flag_col * TILE - self.w + 2
            self.vx = 0
            game.on_flag()

    def _hit_block(self, game, cc, rr):
        ch = game.level.get(cc, rr)
        if ch == "?":
            item = game.level.qcontents.get((cc, rr), "coin")
            game.level.set(cc, rr, "Q")
            if item == "coin":
                game.add_coin(); game.popups.append(CoinPop(cc*TILE, rr*TILE))
                game.audio.play("coin")
            else:
                game.spawn_item(cc, rr, item)
                game.audio.play("power" if item != "star" else "1up")
            game.bumps.append([cc, rr, -4.0])
        elif ch == "B":
            if self.power >= 1:
                game.level.set(cc, rr, "-")
                game.audio.play("brick")
                game.shards.append(BrickShards(cc*TILE, rr*TILE, game.level.theme))
                game.add_score(50)
            else:
                game.audio.play("bump")
                game.bumps.append([cc, rr, -3.0])

    def sprite(self):
        tag = "f" if self.power == 2 else ""
        if self.power == 0:
            if not self.on_ground:
                base = SPR[f"p_small_jump{tag}"]
            elif abs(self.vx) > 0.2:
                base = SPR[f"p_small_walk{int(self.anim)%2}{tag}"]
            else:
                base = SPR[f"p_small_idle{tag}"]
        else:
            if self.crouch:
                base = SPR[f"p_big_crouch{tag}"]
            elif not self.on_ground:
                base = SPR[f"p_big_jump{tag}"]
            elif abs(self.vx) > 0.2:
                base = SPR[f"p_big_walk{int(self.anim)%3}{tag}"]
            else:
                base = SPR[f"p_big_idle{tag}"]
        if self.facing < 0:
            base = pygame.transform.flip(base, True, False)
        return base

    def draw(self, screen, cam):
        if self.invuln > 0 and (self.invuln // 3) % 2 == 0:
            return
        spr = self.sprite()
        sx = int(self.x - cam) - 2
        sy = int(self.y) - (spr.get_height() - self.h)
        if self.star > 0:
            tint = spr.copy()
            cols = [(255,80,80),(255,255,120),(120,255,120),(120,180,255)]
            tint.fill(cols[(self.star//4) % 4] + (90,), special_flags=pygame.BLEND_RGBA_ADD)
            screen.blit(tint, (sx, sy))
        else:
            screen.blit(spr, (sx, sy))


class Goomba(Entity):
    def __init__(self, x, y):
        super().__init__(x, y, 14, 14)
        self.vx = -0.5
        self.anim = 0.0
        self.squish = 0
    def update(self, game):
        if self.squish > 0:
            self.squish -= 1
            if self.squish == 0: self.alive = False
            return
        lvl = game.level
        self.vy = min(self.vy + GRAVITY, MAX_FALL)
        if tile_collide_x(self, lvl):
            self.vx = -self.vx
        for (pc, pt, ph, _) in lvl.pipes:
            pr = pygame.Rect(pc*TILE, pt*TILE, 2*TILE, ph*TILE)
            if self.rect.colliderect(pr):
                self.vx = -self.vx
                self.x += self.vx * 2
        tile_collide_y(self, lvl)
        self.anim += 0.1
        if self.y > NATIVE_H + 40: self.alive = False
    def stomp(self, game):
        self.squish = 18
        self.vx = 0
        self.h = 6; self.y += 8
        game.add_score(100)
        game.audio.play("stomp")
    def draw(self, screen, cam):
        if self.squish > 0:
            spr = SPR["goomba_flat"]
        else:
            spr = SPR[f"goomba{int(self.anim)%2}"]
        screen.blit(spr, (int(self.x - cam) - 1, int(self.y) - (16 - self.h)))


class Koopa(Entity):
    def __init__(self, x, y):
        super().__init__(x, y, 14, 14)
        self.vx = -0.5
        self.anim = 0.0
        self.shell = False
        self.shell_move = 0
        self.facing = -1
    def update(self, game):
        lvl = game.level
        self.vy = min(self.vy + GRAVITY, MAX_FALL)
        sp = self.vx
        if tile_collide_x(self, lvl):
            self.vx = -sp if sp != 0 else self.vx
        for (pc, pt, ph, _) in lvl.pipes:
            pr = pygame.Rect(pc*TILE, pt*TILE, 2*TILE, ph*TILE)
            if self.rect.colliderect(pr):
                self.vx = -self.vx; self.x += self.vx * 2
        tile_collide_y(self, lvl)
        # ledge turn (only when walking)
        if not self.shell or self.shell_move == 0:
            ahead = int((self.cx() + (8 if self.vx > 0 else -8)) // TILE)
            below = int((self.y + self.h + 2) // TILE)
            if self.on_ground and not lvl.solid(ahead, below) and self.vx != 0:
                self.vx = -self.vx
        self.anim += 0.1
        if self.shell_move:
            self._kill_enemies(game)
        if self.y > NATIVE_H + 40: self.alive = False
    def _kill_enemies(self, game):
        for e in game.enemies:
            if e is self or not e.alive: continue
            if self.rect.colliderect(e.rect):
                if isinstance(e, Goomba) and e.squish == 0:
                    e.stomp(game); e.squish = 1; e.alive = False
                    game.add_score(200)
                elif isinstance(e, Koopa) and not (e.shell and e.shell_move == 0):
                    e.alive = False; game.add_score(200)
                elif isinstance(e, Boss):
                    e.hit(game)
    def stomp(self, game):
        if not self.shell:
            self.shell = True; self.shell_move = 0; self.vx = 0
            self.h = 14
            game.audio.play("stomp")
        else:
            self.shell_move = 0; self.vx = 0
            game.audio.play("stomp")
        game.add_score(100)
    def kick(self, game, direction):
        self.shell = True
        self.shell_move = 1
        self.vx = 3.0 * direction
        game.audio.play("kick")
    def draw(self, screen, cam):
        if self.shell:
            spr = SPR["shell"]
        else:
            spr = SPR[f"koopa{int(self.anim)%2}"]
            if self.vx > 0:
                spr = pygame.transform.flip(spr, True, False)
        screen.blit(spr, (int(self.x - cam) - 1, int(self.y) - (16 - self.h)))


class Piranha(Entity):
    def __init__(self, pc, ptop):
        self.base_y = ptop * TILE
        super().__init__(pc*TILE + TILE//2 - 8, self.base_y, 16, 16)
        self.t = random.random() * 120
        self.anim = 0.0
    def update(self, game):
        self.t += 1
        phase = (self.t % 160) / 160.0
        # hide if player is right on the pipe
        px = game.player.cx()
        near = abs(px - (self.x + 8)) < 28
        if phase < 0.4 and not near:
            self.y = self.base_y - int(16 * (phase / 0.4))
        elif phase < 0.6:
            self.y = self.base_y - 16
        elif phase < 1.0 and not near:
            self.y = self.base_y - int(16 * (1 - (phase - 0.6) / 0.4))
        else:
            self.y = self.base_y
        self.anim += 0.08
    def stomp(self, game):  # not stompable
        pass
    def draw(self, screen, cam):
        if self.y < self.base_y:
            spr = SPR[f"piranha{int(self.anim)%2}"]
            screen.blit(spr, (int(self.x - cam), int(self.y)))


class Boss(Entity):
    def __init__(self, x, y):
        super().__init__(x, y, 22, 26)
        self.vx = -0.6
        self.hp = 5
        self.anim = 0.0
        self.flash = 0
    def update(self, game):
        lvl = game.level
        self.vy = min(self.vy + GRAVITY, MAX_FALL)
        if tile_collide_x(self, lvl):
            self.vx = -self.vx
        tile_collide_y(self, lvl)
        # patrol within a range
        if random.random() < 0.01:
            self.vx = -self.vx
        self.anim += 0.08
        if self.flash > 0: self.flash -= 1
    def hit(self, game):
        if self.flash > 0: return
        self.hp -= 1
        self.flash = 20
        game.audio.play("stomp")
        game.add_score(500)
        if self.hp <= 0:
            self.alive = False
            game.add_score(5000)
    def stomp(self, game):
        game.player.hurt(game)
    def draw(self, screen, cam):
        if self.flash > 0 and (self.flash // 2) % 2 == 0:
            return
        s = surf(self.w, self.h)
        rrect(s, C["koopa_dk"], (2, 8, 18, 16), 5)
        rrect(s, C["koopa"], (2, 7, 18, 14), 5)
        rrect(s, C["koopa_sk"], (5, 1, 12, 8), 4)
        pygame.draw.rect(s, C["m_red"], (6, 0, 3, 2))  # horns
        pygame.draw.rect(s, C["m_red"], (13, 0, 3, 2))
        pygame.draw.rect(s, C["black"], (8, 3, 2, 2))
        pygame.draw.rect(s, C["black"], (13, 3, 2, 2))
        pygame.draw.rect(s, C["white"], (7, 6, 8, 2))  # teeth
        screen.blit(s, (int(self.x - cam), int(self.y)))


class Fireball(Entity):
    def __init__(self, x, y, d):
        super().__init__(x, y, 8, 8)
        self.vx = 4.0 * d
        self.vy = 2.0
        self.life = 180
    def update(self, game):
        lvl = game.level
        self.vy = min(self.vy + 0.4, 6)
        if tile_collide_x(self, lvl):
            self.alive = False; return
        if tile_collide_y(self, lvl):
            self.vy = -3.5
        self.life -= 1
        if self.life <= 0 or self.y > NATIVE_H:
            self.alive = False
        for e in game.enemies:
            if e.alive and self.rect.colliderect(e.rect):
                if isinstance(e, Boss):
                    e.hit(game); self.alive = False
                elif isinstance(e, Piranha):
                    pass
                else:
                    e.alive = False; game.add_score(100); self.alive = False
    def draw(self, screen, cam):
        screen.blit(SPR["fireball"], (int(self.x - cam), int(self.y)))


class Item(Entity):
    def __init__(self, x, y, kind):
        super().__init__(x, y, 14, 14)
        self.kind = kind
        self.emerge = 16        # rises out of block
        self.anim = 0.0
        if kind in ("mush", "star", "1up"):
            self.vx = 1.0
    def update(self, game):
        lvl = game.level
        if self.emerge > 0:
            self.y -= 1; self.emerge -= 1
            return
        if self.kind == "flower":
            self.anim += 0.15
            return
        self.vy = min(self.vy + GRAVITY, MAX_FALL)
        if self.kind == "star":
            if self.on_ground: self.vy = -5
        if tile_collide_x(self, lvl):
            self.vx = -self.vx
        for (pc, pt, ph, _) in lvl.pipes:
            pr = pygame.Rect(pc*TILE, pt*TILE, 2*TILE, ph*TILE)
            if self.rect.colliderect(pr):
                self.vx = -self.vx; self.x += self.vx
        tile_collide_y(self, lvl)
        self.anim += 0.1
        if self.y > NATIVE_H + 20: self.alive = False
    def collect(self, game):
        self.alive = False
        p = game.player
        if self.kind == "mush":
            if p.power == 0: p.set_power(1)
            game.add_score(1000); game.audio.play("power")
        elif self.kind == "flower":
            p.set_power(2 if p.power >= 1 else 1)
            game.add_score(1000); game.audio.play("power")
        elif self.kind == "star":
            p.star = 600; game.add_score(1000); game.audio.play("1up")
        elif self.kind == "1up":
            game.lives += 1; game.audio.play("1up")
    def draw(self, screen, cam):
        if self.kind == "mush": spr = SPR["mushroom"]
        elif self.kind == "1up": spr = SPR["oneup"]
        elif self.kind == "flower": spr = SPR[f"flower{int(self.anim)%4}"]
        else: spr = SPR[f"star{int(self.anim)%2}"]
        screen.blit(spr, (int(self.x - cam), int(self.y)))


