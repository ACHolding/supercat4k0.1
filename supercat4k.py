# --- PLAYER PHYSICS: faithful SMB1 fixed-point model -------------------------
# Horizontal speeds/accels are the documented SMB1 values (hex / 4096 = px/frame
# at 60fps). Jump uses SMB1's scheme: a constant launch speed, with gravity
# *latched to a speed tier* at takeoff and lower while the button is held and
# rising -- so a full-run jump goes noticeably higher and farther than a
# standing one, exactly like the original.
P_MIN_WALK  = 0x0130 / 4096    # 0.0742  speed floor when a direction is tapped
P_MAX_WALK  = 0x1900 / 4096    # 1.5625  walk cap (run button up)
P_MAX_RUN   = 0x2900 / 4096    # 2.5625  run cap  (run button held)
P_WALK_ACC  = 0x0098 / 4096    # 0.0371
P_RUN_ACC   = 0x00E4 / 4096    # 0.0557
P_DECEL     = 0x00D0 / 4096    # 0.0508  release friction / ease toward walk cap
