# ----------------------------------------------------------------------------
#  PROCEDURAL AUDIO ENGINE  --  the "math OST engine"
#  Everything (SFX + the full soundtrack) is synthesized from math:
#  square + triangle + noise oscillators rendered to 16-bit PCM buffers.
#  No .mp3 / .wav / external files are loaded -- ever.
#
#  The melodies are ORIGINAL chiptune compositions written for this engine,
#  in the bright NES/Mario *style* (Koji-Kondo-flavoured, copyright-free):
#  bouncy overworld, sparse minor underground, airy athletic, dark castle,
#  a triumphant title fanfare, a star-power rush, and a course-clear jingle.
#  Each stage theme is transposed per world so all 32 levels feel distinct.
# ----------------------------------------------------------------------------
RATE = 22050                       # mixer/render sample rate (chiptune-friendly)
_NIDX = {'C':0, 'C#':1, 'D':2, 'D#':3, 'E':4, 'F':5,
         'F#':6, 'G':7, 'G#':8, 'A':9, 'A#':10, 'B':11}

def NN(s):                         # note name -> MIDI number, e.g. "C4" -> 60
    return 12 * (int(s[-1]) + 1) + _NIDX[s[:-1]]

def _freq(m):                      # MIDI -> Hz (equal temperament)
    return 440.0 * 2 ** ((m - 69) / 12.0)


