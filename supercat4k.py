TILE_CACHE = {}

def make_tile(kind, theme):
    key = (kind, theme)
    if key in TILE_CACHE:
        return TILE_CACHE[key]
    s = surf(TILE, TILE)

    if kind == "ground":
        base, dk, lt = C["ground"], C["ground_dk"], C["ground_lt"]
        if theme == "under":
            base, dk, lt = C["u_brick"], C["u_brick_dk"], C["u_brick_lt"]
        elif theme == "castle":
            base, dk, lt = C["c_brick"], C["c_brick_dk"], C["c_brick_lt"]
        s.fill(base)
        pygame.draw.rect(s, lt, (0, 0, TILE, 2))
        pygame.draw.rect(s, dk, (0, TILE-3, TILE, 3))
        # brick pattern
        for yy in range(0, TILE, 8):
            off = 0 if (yy // 8) % 2 == 0 else 8
            for xx in range(-8, TILE, 16):
                pygame.draw.rect(s, dk, (xx+off, yy, 1, 8))
            pygame.draw.line(s, dk, (0, yy+7), (TILE, yy+7))

    elif kind == "brick":
        base, dk, lt = C["brick"], C["brick_dk"], C["brick_lt"]
        if theme == "under":
            base, dk, lt = C["u_brick"], C["u_brick_dk"], C["u_brick_lt"]
        elif theme == "castle":
            base, dk, lt = C["c_brick"], C["c_brick_dk"], C["c_brick_lt"]
        s.fill(base)
        pygame.draw.rect(s, lt, (0, 0, TILE, 2))
        for yy in range(0, TILE, 8):
            off = 0 if (yy // 8) % 2 == 0 else 8
            pygame.draw.line(s, dk, (0, yy), (TILE, yy))
            pygame.draw.line(s, dk, (off, yy), (off, yy+8))
            pygame.draw.line(s, dk, ((off+8) % 16, yy), ((off+8) % 16, yy+8))

    elif kind in ("qblock", "used"):
        if kind == "qblock":
            base, dk, lt = C["qblock"], C["qblock_dk"], C["qblock_lt"]
        else:
            base, dk, lt = C["used"], C["used_dk"], C["used"]
        rrect(s, dk, (0, 0, TILE, TILE), 2)
        rrect(s, base, (1, 1, TILE-2, TILE-2), 2)
        pygame.draw.rect(s, lt, (2, 2, TILE-4, 2))
        # rivets
        for cx, cy in ((2, 2), (13, 2), (2, 13), (13, 13)):
            pygame.draw.rect(s, dk, (cx, cy, 1, 1))
        if kind == "qblock":
            f = pygame.font.SysFont(None, 0)
            pygame.draw.rect(s, dk, (6, 4, 4, 1))
            pygame.draw.rect(s, dk, (9, 4, 1, 3))
            pygame.draw.rect(s, dk, (7, 7, 2, 1))
            pygame.draw.rect(s, dk, (7, 9, 2, 2))
        TILE_CACHE[key] = s
        return s

    elif kind == "stair":
        s.fill(C["ground"] if theme != "castle" else C["c_brick"])
        pygame.draw.rect(s, C["ground_lt"] if theme != "castle" else C["c_brick_lt"], (0, 0, TILE, 2))
        pygame.draw.rect(s, C["ground_dk"] if theme != "castle" else C["c_brick_dk"], (0, TILE-2, TILE, 2))

    elif kind == "platform":
        s.fill((0, 0, 0, 0))
        rrect(s, C["ground_dk"], (0, 0, TILE, 6), 2)
        rrect(s, C["ground"], (0, 0, TILE, 4), 2)
