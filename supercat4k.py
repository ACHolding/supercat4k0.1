class Audio:
    WORLD_SEMI = [0, 2, -2, 3, 5, -4, 7, 1]      # per-world transposition

    def __init__(self):
        self.ok = False
        self.snd = {}
        self.music_cache = {}
        self.cur = None
        self.mch = None
        try:
            pygame.mixer.pre_init(RATE, -16, 1, 512)
            pygame.mixer.init()
            pygame.mixer.set_num_channels(16)
            self.mch = pygame.mixer.Channel(0)   # reserved for music
            self.ok = True
        except Exception:
            self.ok = False
            return
        self._build_sfx()

    # ---- SFX (short square blips) -------------------------------------------
    def _build_sfx(self):
        self._sfx("jump",  [(523, 30), (784, 60)])
        self._sfx("stomp", [(220, 40), (110, 50)])
        self._sfx("coin",  [(988, 40), (1319, 80)])
        self._sfx("bump",  [(196, 50)])
        self._sfx("power", [(523, 40), (659, 40), (784, 60), (1047, 80)])
        self._sfx("fire",  [(880, 25), (440, 35)])
        self._sfx("kick",  [(330, 30), (165, 40)])
        self._sfx("die",   [(440, 90), (330, 90), (262, 160)])
        self._sfx("1up",   [(659, 40), (784, 40), (988, 40), (1319, 90)])
        self._sfx("flag",  [(523, 40), (659, 40), (784, 40), (1047, 90)])
        self._sfx("brick", [(294, 40), (147, 50)])

    def _blip(self, freq, ms, vol=0.16):
        n = int(RATE * ms / 1000)
        buf = array.array("h")
        period = max(1, int(RATE / max(1, freq)))
        amp = int(32767 * vol)
        for i in range(n):
            fade = 1.0 if i < n - 300 else max(0.0, (n - i) / 300.0)
            v = amp if (i % period) < period // 2 else -amp
            buf.append(int(v * fade))
        return buf

    def _sfx(self, name, notes):
        if not self.ok:
            return
        full = array.array("h")
        for f, ms in notes:
            full.extend(self._blip(f, ms))
        try:
            self.snd[name] = pygame.mixer.Sound(buffer=full.tobytes())
        except Exception:
            self.ok = False

    def play(self, name):
        if self.ok and name in self.snd:
            try:
                self.snd[name].play()
            except Exception:
                pass

    # ---- MUSIC SYNTH --------------------------------------------------------
    def _note(self, note, n, wave, vol, duty, semi):
        if n <= 0:
            return []
        if note is None:
            return [0] * n
        inc = _freq(NN(note) + semi) / RATE
        amp = int(32767 * vol)
        if wave == "square":
            body = [amp if (i * inc) % 1.0 < duty else -amp for i in range(n)]
        elif wave == "tri":
            body = [int(amp * (4 * abs(((i * inc) % 1.0) - 0.5) - 1)) for i in range(n)]
        else:
            body = [0] * n
        a = max(1, int(n * 0.04))                # attack/release to kill clicks
        r = max(1, int(n * 0.12))
        for i in range(min(a, n)):
            body[i] = int(body[i] * i / a)
        for i in range(min(r, n)):
            body[n - 1 - i] = int(body[n - 1 - i] * i / r)
        return body

    def _seq(self, events, bpm, wave, vol, duty, semi):
        sec16 = 60.0 / bpm / 4.0
        out = []
        for note, d in events:
            out.extend(self._note(note, int(RATE * sec16 * d), wave, vol, duty, semi))
        return out

    def _hit(self, kind, n):
        if n <= 0:
            return []
        if kind is None:
            return [0] * n
        if kind == "h":
            L = min(n, int(RATE * 0.02)); amp = 3200
            seg = [random.randint(-amp, amp) for _ in range(L)]
        elif kind == "s":
            L = min(n, int(RATE * 0.06)); amp = 6500
            seg = [random.randint(-amp, amp) for _ in range(L)]
        else:  # kick: short descending sine thump
            L = min(n, int(RATE * 0.07)); amp = 8000
            seg = [int(amp * math.sin(2 * math.pi * (95 * (1 - i / L)) * i / RATE)) for i in range(L)]
        for i in range(L):
            seg[i] = int(seg[i] * (1 - i / L))
        return seg + [0] * (n - L)

    def _drum(self, events, bpm):
        sec16 = 60.0 / bpm / 4.0
        out = []
        for kind, d in events:
            out.extend(self._hit(kind, int(RATE * sec16 * d)))
        return out

    def _mix(self, chans):
        L = min(len(c) for c in chans)
        acc = [0] * L
        for c in chans:
            for i in range(L):
                acc[i] += c[i]
        out = array.array("h")
        for v in acc:
            out.append(32767 if v > 32767 else (-32768 if v < -32768 else v))
        return out

    def _build_track(self, theme, world):
        spec = TRACKS[theme]
        semi = 0 if theme in ("title", "star") else self.WORLD_SEMI[(world - 1) % 8]
        bpm = spec["bpm"]
        chans = [
            self._seq(spec["mel"], bpm, "square", 0.22, spec["duty"], semi),
            self._seq(spec["bass"], bpm, "tri", 0.24, 0.5, semi),
            self._drum(spec["drum"], bpm),
        ]
        return pygame.mixer.Sound(buffer=self._mix(chans).tobytes())

    def _build_jingle(self, name):
        if name == "clear":
            mel = [("C5",2),("D5",2),("E5",2),("F5",2),("G5",2),("A5",2),("B5",2),
                   ("C6",2),("E6",2),("G6",2),("C6",4),("E6",4)]
            bass = [("C3",8),("G3",8),("C3",8),("G3",4),("C3",4)]
            chans = [self._seq(mel, 156, "square", 0.24, 0.5, 0),
                     self._seq(bass, 156, "tri", 0.24, 0.5, 0)]
            return pygame.mixer.Sound(buffer=self._mix(chans).tobytes())
        return None

    def play_music(self, theme, world):
        if not self.ok:
            return
        key = (theme, world)
        if self.cur == key and self.mch.get_busy():
            return
        snd = self.music_cache.get(key)
        if snd is None:
            try:
                snd = self._build_track(theme, world)
            except Exception:
                snd = None
            self.music_cache[key] = snd
        self.cur = key
        if snd:
            try:
                self.mch.play(snd, loops=-1)
            except Exception:
                pass

    def stop_music(self):
        self.cur = None
        if self.mch:
            try:
                self.mch.stop()
            except Exception:
                pass

    def play_jingle(self, name):
        if not self.ok:
            return
        self.stop_music()
        key = ("jingle", name)
        s = self.music_cache.get(key)
        if s is None:
            try:
                s = self._build_jingle(name)
            except Exception:
                s = None
            self.music_cache[key] = s
        if s:
            try:
                s.play()
            except Exception:
                pass


# ----------------------------------------------------------------------------
#  LEVEL
#  Grid of single-char tile codes.  Generated procedurally + seeded.
#    '#' solid ground   'B' breakable brick   '?' question(item)
#    'Q' used block     'S' stair block       'L' lava
#    '|' flagpole       'X' axe/goal (castle)
#  Pipes & special entities are stored separately.
# ----------------------------------------------------------------------------
SOLID = set("#BQ?SX")

THEMES = {1: "over", 2: "under", 3: "sky", 0: "castle"}  # by (level index %4)

class Level:
    def __init__(self, world, stage):
        self.world = world
        self.stage = stage
        sub = stage % 4
        self.theme = THEMES[sub]
        self.is_castle = (sub == 0)
        self.is_under = (sub == 2)
        self.width = 180 + world * 12 + (stage % 4) * 8
        if self.is_castle:
            self.width = 150 + world * 6
        self.grid = [["-"] * self.width for _ in range(ROWS)]
        self.qcontents = {}      # (col,row) -> item string
        self.pipes = []          # (col, top_row, height, piranha_bool)
        self.enemies = []        # (col, row, kind)
        self.flag_col = self.width - 8
        self.bg = self._bg_color()
        self.start_col = 2
        self._generate()

    def _bg_color(self):
        if self.theme == "under":
            return C["u_bg"]
        if self.theme == "castle":
            return C["c_bg"]
        if self.theme == "sky":
            return C["sky_dk"]
        return C["sky"]

    def set(self, col, row, ch):
        if 0 <= col < self.width and 0 <= row < ROWS:
            self.grid[row][col] = ch

    def get(self, col, row):
        if 0 <= col < self.width and 0 <= row < ROWS:
            return self.grid[row][col]
        return "#" if row >= ROWS else "-"

    def solid(self, col, row):
        return self.get(col, row) in SOLID

    def _generate(self):
        rng = random.Random(self.world * 100 + self.stage)
        floor = ROWS - 2       # top of the 2-row ground
        # base ground
        for col in range(self.width):
            self.set(col, ROWS - 1, "#")
            self.set(col, ROWS - 2, "#")

        if self.is_castle:
            self._gen_castle(rng, floor)
            return

        col = 6
        last_pit = 0
        while col < self.flag_col - 6:
            roll = rng.random()
            # gaps / pits (more later in game)
            pit_chance = 0.06 + self.world * 0.012 + (0.05 if self.theme == "sky" else 0)
            if roll < pit_chance and col - last_pit > 6 and col > 16:
                gap = rng.randint(2, 3 if self.world < 4 else 4)
                for g in range(gap):
                    self.set(col + g, ROWS - 1, "-")
                    self.set(col + g, ROWS - 2, "-")
                col += gap + rng.randint(2, 4)
                last_pit = col
                continue

            if roll < 0.18:
                # question / brick row
                h = floor - rng.randint(3, 4)
                length = rng.randint(1, 4)
                for i in range(length):
                    c = col + i
                    if i == length // 2 and rng.random() < 0.55:
                        self.set(c, h, "?")
                        r = rng.random()
                        if r < 0.65:
                            self.qcontents[(c, h)] = "coin"
                        elif r < 0.86:
                            self.qcontents[(c, h)] = "mush"
                        elif r < 0.96:
                            self.qcontents[(c, h)] = "flower"
                        else:
                            self.qcontents[(c, h)] = "star"
                    else:
                        self.set(c, h, "B")
                if rng.random() < 0.4:
                    self.enemies.append((col + length + 2, floor - 1, "goomba"))
                col += length + rng.randint(2, 4)

            elif roll < 0.30:
                # pipe (maybe with piranha)
                ph = 1  # Mario's small height in tiles (14px → 1 tile)
                pir = rng.random() < 0.5
                self.pipes.append((col, ROWS - 2 - ph, ph, pir))
                col += rng.randint(4, 7)

            elif roll < 0.42:
                # floating platform of bricks
                h = floor - rng.randint(4, 6)
                length = rng.randint(3, 6)
                for i in range(length):
                    self.set(col + i, h, "B")
                if rng.random() < 0.5:
                    self.enemies.append((col + 1, h - 1, "koopa"))
                col += length + rng.randint(3, 5)

            elif roll < 0.55:
                # enemies on the ground
                n = rng.randint(1, 2 + self.world // 3)
                for i in range(n):
                    kind = "koopa" if rng.random() < 0.35 else "goomba"
                    self.enemies.append((col + i * 2, floor - 1, kind))
                col += n * 2 + rng.randint(3, 6)
            else:
                col += rng.randint(2, 5)

        # ending staircase + flagpole
        sc = self.flag_col - 10
        for i in range(7):
            for j in range(i + 1):
                self.set(sc + i, floor - j, "S")
        # flagpole
        fc = self.flag_col
        for r in range(2, floor):
            self.set(fc, r, "|")
        self.set(fc, 1, "|")

    def _gen_castle(self, rng, floor):
        col = 6
        while col < self.width - 14:
            roll = rng.random()
            if roll < 0.22 and col > 12:
                # lava pit
                gap = rng.randint(2, 3)
                for g in range(gap):
                    self.set(col + g, ROWS - 1, "L")
                    self.set(col + g, ROWS - 2, "L")
                col += gap + rng.randint(3, 5)
            elif roll < 0.45:
                h = floor - rng.randint(3, 5)
                for i in range(rng.randint(2, 4)):
                    self.set(col + i, h, "B")
                col += rng.randint(4, 7)
            elif roll < 0.62:
                self.enemies.append((col, floor - 1, "koopa"))
                col += rng.randint(4, 6)
            else:
                col += rng.randint(3, 6)
        # goal axe / bridge at the end
        gx = self.width - 10
        for r in range(floor - 1, ROWS):
            for c in range(gx - 2, gx + 4):
                if r == floor - 1:
                    self.set(c, r, "#")
        self.flag_col = gx
        for r in range(2, floor):
            self.set(gx, r, "|")
        self.set(gx, 1, "X")
        # boss
        self.enemies.append((gx - 6, floor - 1, "boss"))


